<p>Domstry is a domain database for SEO, Marketing, Security, Leads, Statistic, etc... A Crawler is visiting domains and gathering all details. If you need any extra detail just ask, we can add it for everyone's benefit.</p>
<p class="mt-3">The crawler visits domain homepage only. For each domain, the bot is collecting:</p>

    <ul class="list-disc pl-3 ml-8 mt-3">
        <li>Title & Description Meta tags </li>
        <li>Technologies used by analyzing server response</li>
        <li>Creation & Expiration Date from whois</li>
        <li>Http Response code</li>
        <li>Dns / Hosting / Network</li>
        <li>Time to first byte</li>
        <li>Contact Details: Emails & Phone numbers</li>        
        <li>External links</li>
        <li>Domain Availability from GoDaddy's API</li>
        <li>Page Rank and ranking from open database</li>
        <li>Country and continent from IP</li>

    </ul>

